using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Operaciones
{
    public class Operaciones
    {
        public Operaciones() { }

        public int Suma(int x, int y)
        {
            return (x + y);
        }

        public int Mult(int x, int y)
        {
            return (x * y);
        }

        public int Resta(int x, int y)
        {
            return (x - y);
        }
    }
}
